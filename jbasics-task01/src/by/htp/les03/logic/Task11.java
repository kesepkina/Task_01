package by.htp.les03.logic;

public class Task11 {

	public static void task() {

		double a, b, c, s, p;

		a = 2;
		b = 3;

		c = Math.sqrt(Math.pow(a, 2) + Math.pow(b, 2));

		s = a * b / 2;
		p = a + b + c;

		System.out.println("11. Площадь и периметр прямоугольного треугольника со сторонами " + a + " и " + b
				+ " равны соответственно " + s + " и " + p);

	}

}
