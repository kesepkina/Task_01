package by.htp.les02.main;

public class Task01 {

	public static void main(String[] args) {

		double x;
		double y;

		double sum, dev, mul, dif;

		x = 2;
		y = 5;

		sum = x + y;
		dif = x - y;
		dev = x / y;
		mul = x * y;

		System.out.println(x + " + " + y + " = " + sum);
		System.out.println(x + " - " + y + " = " + dif);
		System.out.println(x + " * " + y + " = " + mul);
		System.out.println(x + " / " + y + " = " + dev);

	}

}
